import math

####################################################################################################

class Gaussian(object):

    ##############################################

    def __init__(self, mean, sigma):

        self._mean = mean
        self._sigma = sigma
        self._inverse_sigma = 1. / self.sigma

    ##############################################

    @property
    def mean(self):
        return self._mean

    @property
    def sigma(self):
        return self._sigma

    ##############################################

    def __call__(self, x):

        return math.exp(-((self._mean - x)*self._inverse_sigma)**2)

